package com.app;

public class Calculator {

    // Add two numbers
    public int sum(int a, int b) {
        return a + b;
    }

    // Multiply two numbers
    public int product(int a, int b) {
        return a * b;
    }

    // Compare two numbers
    public boolean compare(int a, int b) {
        return a < b ? true : false;
    }

}
